package Start;

public class Hello { // hello 프로젝트 이름
	public static void main(String[] args) { // public static void main부터 명령이 시작하는 시점
		System.out.println("H"); // ; 한개의 명령이 끝남, println은 한줄(ln)로 출력
		System.out.println("e"); // ; () 동작 {} 동작의 시작, 블락, A.B A안에 B라는 뜻
		System.out.println("l"); // "" 사이의 내용은 사람이 쓰는 글씨, 그대로 반환
		System.out.println("l"); 
		System.out.println("o,"); 
		System.out.println("JAVA!"); 
 	}
}
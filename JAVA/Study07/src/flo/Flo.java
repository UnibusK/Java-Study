package flo;

public class Flo {
	public static void main (String[] args) {
		//float : 플로트, 짧은 소수점을 저정할 수 있는 규격
		//숫자 뒤에 소문자 또는 대문자 F를 붙인다
		float f;
		f=3.14F;
		System.out.println(f);
		
		//double : 더블, 긴 소수점을 저정할 수 있는 규격
		//소문자 또는 대문자 D를 붙여도 되고 안붙여도 된다.
		double d;
		d=34.123541;
		System.out.println(d);
	}
}
package cha;

public class Cha {
	public static void main (String[] args) {
		//char : 캐릭터, 한개의 문자를 저장할 수 있는 규격
		char c='안';
		char d='녕';
		char e='하';
		char f='세';
		char g='요';
		System.out.print(c);
		System.out.println(d);
		System.out.print(e);
		System.out.print(f);
		System.out.println(g);
		
		//String : 스트링, 여러개의 문자(문자열)를 저장할 수 있는 규격
		String s;
		s="반값습니다.";
		System.out.println(s);
		String S="Your WELCOME";
		System.out.println(S);
	}
}
